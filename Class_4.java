public class Class_4 {
    public static void main(String[] args){
        // Defining variable int,bool,char and float 
        int num1=45;// Stores an integer value
        boolean b=true;// Stores either true or false values
        char alphabet='A'; // Stores a single character/letter or Ascii value
        float c=31.4f;// Stores fractional numbers but requires f at its end
        System.out.println(num1);
        System.out.println(b);
        System.out.println(alphabet);
        System.out.println(c);
    }
}
